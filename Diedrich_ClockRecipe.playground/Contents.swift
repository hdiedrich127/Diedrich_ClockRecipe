Clock Recipe

Ingredients:
1. Apple's timer class
2. seconds variable
3. minutes variable
4. hours variable

Directions:
1. When the program starts the clock will trigger and start a count
2. Add 1 to seconds until it reaches 60 then reset
3. When seconds resets add 1 to minutes
4. When minutes equals 60 add 1 to hours and reset minutes to 0
